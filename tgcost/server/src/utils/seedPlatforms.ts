async function seedPlatforms() {
  try {
    const connection = await mysql.createConnection({
      host: process.env.DB_HOST || 'localhost',
      user: process.env.DB_USER || 'root',
      password: process.env.DB_PASSWORD || '',
      database: dbName,
    });

    console.log('🌱 Seeding platforms...');

    for (const platform of platforms) {
      const id = uuidv4();
      await connection.execute(
        `INSERT INTO platforms (id, name, type, address, city, latitude, longitude, price_per_day, rating, reviews_count,
         image, images, description, size, format, illumination, traffic, available, owner_id, status)
         VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
         ON DUPLICATE KEY UPDATE name=name`,
        [
          id,
          platform.name,
          platform.type,
          platform.address,
          platform.city,
          platform.latitude,
          platform.longitude,
          platform.pricePerDay,
          platform.rating,
          0,
          platform.image,
          JSON.stringify(platform.images),
          platform.description,
          platform.size,
          platform.format,
          platform.illumination,
          platform.traffic,
          true,
          platform.ownerId,
          'active',
        ]
      );
      console.log(`  ✅ ${platform.name}`);
    }

    await connection.end();
    console.log('');
    console.log('🎉 Platforms seeded successfully!');
    console.log(`📊 Total: ${platforms.length} platforms`);
    process.exit(0);
  } catch (error) {
    console.error('❌ Failed to seed platforms:', error);
    process.exit(1);
  }
}

seedPlatforms();
